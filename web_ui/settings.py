def _parse_path_list_env(name: str, default: list[Path]) -> list[Path]:
    raw = os.getenv(name, "").strip()
    if not raw:
        return default
    paths: list[Path] = []
    for item in raw.split(","):
        value = item.strip()
        if not value:
            continue
        paths.append(Path(value).expanduser())
    return paths or default


