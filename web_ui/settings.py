def _parse_cursor_cli_extra_env():
    """Extra env vars for Cursor CLI. When CURSOR_CLI_HTTP_1 is True, adds CURSOR_DISABLE_HTTP2=1.
    CURSOR_API_KEY (лаЛЋлАРђџламЉла┬хла┬╗лАлілалЁлаЛЋ лалє .env) л▓лѓРђЮ headless-лалєлАРђдлаЛЋламЉ ла┬▒ла┬хла┬и Google; лаЛћла┬╗лАлІлАРђА лалє Cursor л▓РђаРђЎ Settings л▓РђаРђЎ API Access.
    Override via CURSOR_CLI_EXTRA_ENV JSON, e.g. CURSOR_CLI_EXTRA_ENV='{"KEY":"VALUE"}'."""
    raw = os.getenv("CURSOR_CLI_EXTRA_ENV", "").strip()
    if raw:
        try:
            d = json.loads(raw)
            return dict(d) if isinstance(d, dict) else {}
        except json.JSONDecodeError:
            return {}
    if CURSOR_CLI_HTTP_1:
        return {"CURSOR_DISABLE_HTTP2": "1"}
    return {}


